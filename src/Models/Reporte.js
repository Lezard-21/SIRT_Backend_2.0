class Reporte{
    constructor(id, programaEdu, periodo, noTutoria, fecha, noAsistenciaAlum, noAlumRiesgo, comentarios){
        this.id = id;
        this.programaEdu = programaEdu;
        this.periodo = periodo;
        this.noTutoria = noTutoria;
        this.fecha = fecha;
        this.noAsistenciaAlum = noAsistenciaAlum;
        this.noAlumRiesgo = noAlumRiesgo;
        this.comentarios = comentarios;
    }
}